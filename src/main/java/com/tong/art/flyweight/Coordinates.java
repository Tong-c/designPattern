package com.tong.art.flyweight;

/**
 * @Description: 坐標類：外部狀態類
 * @Create: 2018/10/20 0020 下午 6:18
 */
public class Coordinates {
    private int x;
    private int y;

    public Coordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
